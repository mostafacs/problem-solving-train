    public static void main(String[] args) {
        InputStream inputStream = System.in;
        OutputStream outputStream = System.out;
        InputReader in = new InputReader(inputStream);
        OutputWriter out = new OutputWriter(outputStream);
        Pylons solver = new Pylons();
        int testCount = Integer.parseInt(in.next());
        for (int i = 1; i <= testCount; i++) {
            solver.solve(i, in, out);
        }
        out.close();
    }

    static class Pylons {
        Graph graph;
        boolean[] done;
        int[] order;

        public void solve(int testNumber, InputReader in, OutputWriter out) {
            int r = in.readInt();
            int c = in.readInt();
            if (r == 2 && c <= 4) {
                out.printLine("Case #" + testNumber + ": IMPOSSIBLE");
                return;
            }
            if (c == 2 && r <= 4) {
                out.printLine("Case #" + testNumber + ": IMPOSSIBLE");
                return;
            }
            if (r == 3 && c == 3) {
                out.printLine("Case #" + testNumber + ": IMPOSSIBLE");
                return;
            }
            Random random = new Random(239);
            int[] o = ArrayUtils.createOrder(r * c);
            for (int i = 0; i < r * c; i++) {
                int at = random.nextInt(i + 1);
                int temp = o[at];
                o[at] = o[i];
                o[i] = temp;
            }
            graph = new Graph(r * c);
            for (int i = 0; i < r * c; i++) {
                int a = i / c;
                int b = i % c;
                for (int jj = 0; jj < r * c; jj++) {
                    int j = o[jj];
                    int x = j / c;
                    int y = j % c;
                    if (a == x || b == y || a + b == x + y || a - b == x - y) {
                        continue;
                    }
                    graph.addSimpleEdge(i, j);
                }
            }
            done = new boolean[r * c];
            order = new int[r * c];
            done[c + 1] = true;
            order[0] = c + 1;
//        System.err.println(r + " " + c);
            if (!go(1, c + 1)) {
                throw new RuntimeException();
            }
            out.printLine("Case #" + testNumber + ": POSSIBLE");
            for (int i = 0; i < r * c; i++) {
                out.printLine(order[i] / c + 1, order[i] % c + 1);
            }
        }

        private boolean go(int step, int last) {
            if (step == order.length) {
                return true;
            }
            for (int i = graph.firstOutbound(last); i != -1; i = graph.nextOutbound(i)) {
                int next = graph.destination(i);
                if (!done[next]) {
                    order[step] = next;
                    done[next] = true;
                    if (go(step + 1, next)) {
                        return true;
                    }
                    done[next] = false;
                }
            }
            return false;
        }

    }

    static class ArrayUtils {
        public static int[] range(int from, int to) {
            return Range.range(from, to).toArray();
        }

        public static int[] createOrder(int size) {
            return range(0, size);
        }

    }

    static interface IntStream extends Iterable<Integer>, Comparable<IntStream> {
        public IntIterator intIterator();

        default public Iterator<Integer> iterator() {
            return new Iterator<Integer>() {
                private IntIterator it = intIterator();

                public boolean hasNext() {
                    return it.isValid();
                }

                public Integer next() {
                    int result = it.value();
                    it.advance();
                    return result;
                }
            };
        }

        default public int compareTo(IntStream c) {
            IntIterator it = intIterator();
            IntIterator jt = c.intIterator();
            while (it.isValid() && jt.isValid()) {
                int i = it.value();
                int j = jt.value();
                if (i < j) {
                    return -1;
                } else if (i > j) {
                    return 1;
                }
                it.advance();
                jt.advance();
            }
            if (it.isValid()) {
                return 1;
            }
            if (jt.isValid()) {
                return -1;
            }
            return 0;
        }

    }

    static abstract class IntAbstractStream implements IntStream {
        public String toString() {
            StringBuilder builder = new StringBuilder();
            boolean first = true;
            for (IntIterator it = intIterator(); it.isValid(); it.advance()) {
                if (first) {
                    first = false;
                } else {
                    builder.append(' ');
                }
                builder.append(it.value());
            }
            return builder.toString();
        }

        public boolean equals(Object o) {
            if (!(o instanceof IntStream)) {
                return false;
            }
            IntStream c = (IntStream) o;
            IntIterator it = intIterator();
            IntIterator jt = c.intIterator();
            while (it.isValid() && jt.isValid()) {
                if (it.value() != jt.value()) {
                    return false;
                }
                it.advance();
                jt.advance();
            }
            return !it.isValid() && !jt.isValid();
        }

        public int hashCode() {
            int result = 0;
            for (IntIterator it = intIterator(); it.isValid(); it.advance()) {
                result *= 31;
                result += it.value();
            }
            return result;
        }

    }

